package Matematika;

/**
 *
 * @author FAJAR
 */
public class Matematika {
     int a;
    int b;
    
    public void setNilai(int nilaiA, int nilaiB) {
        a = nilaiA;
        b = nilaiB;
    }
    
    public int pertambahan() {
        return a + b;
    }
    
    public int pengurangan() {
        return a - b;
    }
    
    public int perkalian() {
        return a * b;
    }
    
    public int pembagian() {
        return a / b;
    }
}
